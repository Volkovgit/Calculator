package org.example;

public class Calculator {
    public static float sum(int x, int y) {
        return x + y;
    }

    public static float sub(int x, int y) {
        return x - y;
    }

    public static float mult(int x, int y) {
        return x * y;
    }

    public static float div(int x, int y) {
        return (float) x / y;
    }
}